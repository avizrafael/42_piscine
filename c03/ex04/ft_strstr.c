#include <unistd.h>

char	*ft_strstr(char *str, char *to_find)
{
	int	i;
	int	j;

	i = 0;
	if (to_find[0] == '\0')
		return (str);
	while (str[i] != '\0')
	{
		j = 0;
		while (str[i + j] != '\0' && str[i + j] == to_find[j])
		{
			if (to_find[j + 1] == '\0')
				return (&str[i]);
			++j;
		}
		++i;
	}
	return (0);
}

#include <stdio.h>
int	main(void)
{
	char *s1 = "needle in a haystack";
	char *s2 = "in";
	char *result;

	result = ft_strstr(s1, s2);
	printf("%s", result);
	return (0);
}