#include <unistd.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	return (i);
}

int	main(void)
{
	char	x[] = "Hello s 5";
	int	y;

	y = ft_strlen(x);
	printf("%s %i",x , y);
}